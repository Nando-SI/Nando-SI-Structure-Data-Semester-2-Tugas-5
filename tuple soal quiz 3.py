# membuat tuple dan perhitungan
angka = (10,20,30,40,50) #membuat tuple yang berisi beberapa angka

#perhitungan pada tuple
jumlah = sum(angka) #menghitung jumlah semua angka dalam tuple
maksimum = max(angka) #menghitung nilai maksimum dalam tuple
minimum = min(angka) #menghitung nilai minimum dalam tuple

# menampilkan hasil perhitungan
print("Jumlah:", jumlah) #menampilkan jumlah semua angka dalam tuple
print("Maksimum:", maksimum) #menampilkan nilai maksimum dalam tuple
print("Minimum:", minimum) #menampilkan nilai minimum dalam tuple
# output:
# Jumlah: 150
# Maksimum: 50
# Minimum: 10