# membuat dictionary dengan data mahasiswa
mahasiswa = { 
    "nama": "Andi", # key "nama" value "Andi"
    "NIM": "12345", # key "NIM" value "12345"
    "jurusan": "informatika" # key "jurusan" value "informatika"
}
# mengakses nilai dalam dictionary
print(mahasiswa["jurusan"]) # mengakses nilai dengan key "jurusan"
#output: informatika